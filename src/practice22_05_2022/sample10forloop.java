package practice22_05_2022;

public class sample10forloop
{
	public static void main(String[] args) 
	{

		
		for(int i=1; i<=5 ;i++)			// asending
		{
			System.out.println(i);
		}
		
		for(int j=5;j>=1;j--)			//reverse	/ desending
		{
			System.out.println(j);
		}
		
		for(int i=1;i<=20;i=i+2)		// odd
		{
			System.out.println(i);
		}
		
		for(int i=2;i<=20;i=i+2)			// even
		{
			System.out.println(i);
		}
		
		for(int i=1;i<=7;i++)			// square
		{
			System.out.println(i*i);
		}
		
		for(int i=1;i<=4;i++)			// multiple time message
		{
			System.out.println("Hi");	
		}
		
		for(int i=1;i<=6;i++)
		{
			System.out.println(i*i*i);
		}
		
	}

}
