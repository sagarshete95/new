package practice22_05_2022;

public class sample10whileloop 
{
	public static void main(String[] args) 
	{
		int i=1;
		while (i<=5)
			
		{
			System.out.println("hi");
			i++;
		}
		
		int j=1;
		while(j<=5)
		{
			System.out.println(j);
			j++;
		}
		
		int k=5;
		while(k>=1)
		{
			System.out.println(k+" hi");
			k--;
		}
		
		int l=9;
		while(l>=1)
		{System.out.println(l);
		l=l-2;
		}
		int b=2;
		while(b<=20)
		{
			System.out.println(b);
			b=b+2;
		}
		
		
		
		
	}

}
